    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String aux=jComboBox1.getItemAt(jComboBox1.getSelectedIndex());
        for(Atividade a:main.atividades)
        {
            if(a.getNome().equals(aux) && a.getStatus()=='a')
            {
                a.setStatus('c');
                JOptionPane.showMessageDialog(null, "Atividade concluida.");
                break;
            }
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jComboBox2.removeAllItems();
        int i = 0;
        for (Disciplina a : main.disciplinas) {
            if (funcoes.Usuario_atual.equals(a.getnomeuser())) {
                
                if (a.verificaDisciplina(Compromissos.data.getDay())) {
                    jComboBox2.addItem(a.getNome());
                }
            }
            i++;
        }
        i=0;
        jComboBox1.removeAllItems();
        for (Atividade a : main.atividades) {
            if (funcoes.Usuario_atual.equals(a.getNomeUser()) && a.getStatus()=='a') {
                if (a.getData().getDate() == Compromissos.data.getDate() && a.getData().getMonth() == Compromissos.data.getMonth() && a.getData().getYear() == (Compromissos.data.getYear())) {

                    jComboBox1.addItem(a.getNome());
                }
            }
            i++;
        }
    }//GEN-LAST:event_formWindowOpened

